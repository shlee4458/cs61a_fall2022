def sum_nums(s):
    """
    >>> a = Link(1, Link(6, Link(7)))
    >>> sum_nums(a)
    14
    """
    sum = 0
    while not isinstance(s.rest, tuple):
        sum += s.first
        s = s.rest
    sum += s.first

    return sum
   
